            </p>
            <h4 class="div-left-h3" id="Bước_4:_Thẩm_định_hình_thức_đơn_đăng_ký">Bước
                4:Thẩm định hình thức đơn đăng ký</h4>
            <p>Thời hạn thẩm định hình thức đơn đăng ký sáng chế
                là 01 tháng kể từ ngày nộp đơn.</p>
            <h4 class="div-left-h3" id="Bước_5:_Công_bố_đơn_đăng_ký_sáng_chế">Bước 5: Công
                bố đơn đăng ký sáng chế</h4>
            <ul class="div-left-ul">
                <li>Đơn đăng ký sáng chế/giải pháp hữu ích được công bố
                    trong tháng thứ mười chín kể từ ngày ưu tiên hoặc
                    ngày nộp đơn. Nếu đơn không có ngày ưu tiên hoặc
                    trong thời hạn 02 tháng kể từ ngày chấp nhận đơn hợp
                    lệ, tùy theo ngày nào muộn hơn;</li>
                <li>Đơn đăng ký sáng chế theo Hiệp ước hợp tác về sáng
                    chế (sau đây gọi là “đơn PCT”) được công bố trong
                    thời hạn 02 tháng kể từ ngày chấp nhận đơn hợp lệ đã
                    vào giai đoạn quốc gia;</li>
                <li>Đơn đăng ký sáng chế/giải pháp hữu ích có yêu cầu
                    công bố sớm được công bố trong thời hạn 02 tháng kể
                    từ ngày Cục Sở hữu trí tuệ nhận được yêu cầu công bố
                    sớm. Hoặc kể từ ngày chấp nhận đơn hợp lệ, tùy theo
                    ngày nào muộn hơn. Để được công bố sớm, chủ đơn cần
                    có Văn bản yêu cầu công bố sớm. Trong đó, chủ đơn
                    cần nêu rõ lý do cần công bố sớm. Yêu cầu công bố
                    sớm không phải nộp phí, lệ phí.</li>
            </ul>
            <h4 class="div-left-h3" id="Bước_6:_Thẩm_định_nội_dung">Bước 6: Thẩm định
                nội dung</h4>
            <ul class="div-left-ul">
                <li>Thẩm định nội dung: không quá mười tám tháng, kể từ
                    ngày công bố đơn nếu yêu cầu thẩm định nội dung được
                    nộp trước ngày công bố đơn hoặc kể từ ngày nhận được
                    yêu cầu thẩm định nội dung nếu yêu cầu đó được nộp
                    sau ngày công bố đơn.</li>
                <li>Kết thúc thẩm định nội dung Cục Sở hữu trí tuệ sẽ ra
                    Thông báo dự định cấp văn bằng bảo hộ độc quyền sáng
                    chế. Hoặc thông báo từ chối cấp bằng bảo hộ sáng chế
                    và lý do.</li>
            </ul>
            <h4 class="div-left-h3" id="Bước_7:_Nộp_lệ_phí_cấp_văn_bằng_bảo_hộ">Bước 7: Nộp
                lệ phí cấp văn bằng bảo hộ</h4>
            <p>Sau khi nhận được thông báo dự định cấp văn bằng bảo hộ,
                chủ đơn tiến hành nộp lệ phí cấp bằng.</p>
            <h4 class="div-left-h3" id="Bước_8:_Cấp_Văn_bằng_bảo_hộ_độc_quyền_sáng_chế">Bước
                8: Cấp Văn bằng bảo hộ độc quyền sáng chế</h4>
            <p>Sau khi lệ phí vấp văn bằng trong khoảng từ 02-03 tháng
                kể từ ngày nộp lệ phí cấp văn bằng Cục Sở hữu trí tuệ sẽ
                cấp văn bằng bảo hộ độc quyền sáng chế.
            </p>
            <h4 class="div-left-h3" id="Bước_9:_Nộp_phí_duy_trì_hiệu_lực_bằng_độc_quyền_sáng_chế_hàng_năm">Bước
                9: Nộp phí duy trì hiệu lực bằng độc quyền sáng chế hàng
                năm</h4>
            <ul class="div-left-ul">
                <li>Bằng độc quyền Sáng chế có hiệu lực kể từ ngày cấp
                    kéo dài đến hết 20 năm kể từ ngày nộp đơn/ngày nộp
                    đơn quốc tế. Hiệu lực bảo hộ cần được duy trì hàng
                    năm.</li>
                <li>Bằng độc quyền Giải pháp hữu ích có hiệu lực kể từ
                    ngày cấp kéo dài đến hết 10 năm kể từ ngày nộp
                    đơn/ngày nộp đơn quốc tế. Hiệu lực bảo hộ cần được
                    duy trì hàng năm.</li>
                <li>Để được duy trì hiệu lực văn bằng bảo hộ, trong vòng
                    06 tháng trước khi kết thúc kỳ hạn hiệu lực chủ văn
                    bằng bảo hộ phải nộp yêu cầu duy trì hiệu lực. Đơn
                    duy trì hiệu lực có thể nộp muộn nhưng không quá 06
                    tháng kể từ ngày kết thúc kỳ hạn hiệu lực và chủ văn
                    bằng bảo hộ phải nộp thêm lệ phí duy trì hiệu lực
                    muộn.</li>
            </ul>
            <p>Hồ sơ yêu cầu duy trì hiệu lực bằng sáng chế gồm:</p>
            <ul class="div-left-ul">
                <li>Tờ khai yêu cầu duy trì hiệu lực (theo mẫu
                    02-GH/DTVB tại Phụ lục C của Thông tư 01);</li>
                <li>Giấy ủy quyền (trường hợp nộp yêu cầu thông qua đại
                    diện);</li>
                <li>Bản sao chứng từ nộp phí, lệ phí theo quy định;</li>
                <li>Tài liệu khác (nếu cần).</li>
            </ul>
            <p>Phí, lệ phí duy trì hiệu lực bằng sáng chế</p>
            <ul class="div-left-ul">
                <li>Lệ phí duy trì hiệu lực: 100.000 đồng/điểm.</li>
                <li>Lệ phí duy trì hiệu lực muộn: 10% lệ phí duy trì/mỗi
                    tháng nộp muộn.</li>
                <li>Phí thẩm định yêu cầu duy trì: 160.000 đồng/ văn
                    bằng bảo hộ.</li>
                <li>Phí sử dụng văn bằng bảo hộ (theo năm):</li>
                <li>Năm 1 -2: 300.000 đồng/năm/điểm.</li>
                <li>Năm 3-4: 500.000 đồng/năm/điểm.</li>
                <li>Năm 5-6: 800.000 đồng/năm/điểm.</li>
                <li>Năm 7-8: 1.200.000 đồng/năm/điểm.</li>
                <li>Năm 9-10: 1.800.000 đồng/năm/điểm.</li>
                <li>Năm 11-13: 2.500.000 đồng/năm/điểm.</li>
                <li>Năm 14-16: 3.300.000 đồng/năm/điểm.</li>
                <li>Năm 17-20: 4.200.000 đồng/năm/điểm.</li>
                <li>Phí công bố Thông báo ghi nhận duy trì: 120.000
                    đồng/đơn.</li>
                <li>Phí đăng bạ thông tinduy trì hiệu lực: 120.000
                    đồng/ văn bằng bảo hộ.</li>
            </ul>
            <h3 class="div-left-h3" id="Thời_hạn_thực_hiện_thủ_tục_đăng_ký_sáng_chế">Thời
                hạn thực hiện thủ tục đăng ký sáng chế</h3>
            <ul class="div-left-ul">
                <li>Thời hạn xin hưởng quyền ưu tiên theo Công ước Paris
                    cho đơn sáng chế/giải pháp hữu ích là 12 tháng tính
                    từ ngày ưu tiên.</li>
                <li>Thời hạn để đơn PCT nộp vào quốc gia Việt Nam là 31
                    tháng tính từ ngày ưu tiên.</li>
                <li>Trong những trường hợp cần thiết, Cục Sở hữu trí tuệ
                    Việt Nam có thể yêu cầu bổ sung Giấy xác nhận việc
                    chuyển nhượng. Bản dịch tiếng Việt của tài liệu xin
                    hưởng quyền ưu tiên.</li>
                <li>Thời hạn để nộp yêu cầu xét nghiệm nội dung là 42
                    tháng (đối với đơn xin cấp Bằng độc quyền sáng chế)
                    hoặc 36 tháng (đối với đơn xin cấp Bằng độc quyền
                    giải pháp hữu ích) tính từ ngày ưu tiên sớm nhất.</li>
                <li>Thời hạn cho việc xét nghiệm hình thức của đơn là 01
                    tháng tính từ ngày nộp đơn.</li>
                <li>Thời hạn cho việc xét nghiệm nội dung là 31 tháng
                    (đối với đơn xin cấp Bằng độc quyền sáng chế). 09
                    tháng (đối với đơn xin cấp Bằng độc quyền giải pháp
                    hữu ích).</li>
                <li>Công bố đơn (nếu yêu cầu xét nghiệm nội dung được
                    nộp trước ngày Công bố đơn) hoặc tính từ ngày nộp
                    yêu cầu xét nghiệm nội dung (nếu yêu cầu xét nghiệm
                    nội dung đó được nộp sau ngày Công bố đơn);</li>
                <li>Thời hạn hiệu lực của Bằng độc quyền sáng chế là 20
                    năm và của Bằng độc quyền giải pháp hữu ích là 10
                    năm tính từ ngày nộp đơn hợp lệ. Các Bằng độc quyền
                    này có hiệu lực kể từ ngày cấp;</li>
                <li>Chỉ phải nộp phí duy trì hiệu lực sau khi Bằng độc
                    quyền sáng chế/giải pháp hữu ích đã được cấp.</li>
            </ul>
            <h3 class="div-left-h3" id="Quyền_ưu_tiên_và_nguyên_tắc_nộp_đơn_đầu_tiên_trong_đăng_ký_sáng_chế">Quyền
                ưu tiên và nguyên tắc nộp đơn đầu tiên trong đăng ký
                sáng chế</h3>
            <p>Theo Luật Sở hữu trí tuệ, quyền đối với sáng chế được xác
                lập trên cơ sở quyết định cấp văn bằng bảo hộ của Cục Sở
                hữu trí tuệ. Người muốn được hưởng quyền đối với sáng
                chế phải trực tiếp làm đơn xin cấp văn bằng bảo hộ và
                nộp cho Cục Sở hữu trí tuệ hoặc. Người nộp đơn uỷ quyền
                cho một Tổ chức đại diện sở hữu công nghiệp đã được cấp
                chứng chỉ hành nghề như Công tyLuật Việt
                Anđể thực hiện các công việc liên quan.
            </p>
            <h3 class="div-left-h3" id="Câu_hỏi_liên_quan_đến_đăng_ký_sáng_chế">Câu hỏi liên
                quan đến đăng ký sáng chế</h3>
            <p><strong>Sáng chế là gì?</strong></p>
            <p>Sáng chế là giải pháp kỹ thuật dưới dạng sản phẩm hoặc
                quy trình nhằm giải quyết một vấn đề xác định bằng việc
                ứng dụng các quy luật tự nhiên.
            </p>
            <p><strong>Sáng chế được bảo hộ trong bao lâu?</strong></p>
            <p>Sáng chế được bảo hộ tối đa 20 năm. Tuy nhiên để duy trì
                hiệu lực văn bằng bảo hộ hàng năm chủ sở hữu phải đóng
                phí duy trì hiệu lực văn bằng bảo hộ.
            </p>
            <h3 class="div-left-h3" id="Nên_đăng_ký_sáng_chế_như_thế_nào?">Nên đăng ký sáng
                chế như thế nào?</h3>
            <p>Đăng ký sáng chế có thể được thực hiện bằng một trong
                hình thức:</p>
            <ul class="div-left-ul">
                <li>Nộp đơn đăng ký trực tiếp tại Cục sở hữu trí tuệ;</li>
                <li>Đăng ký thông quaTổ chức Đại diện sở hữu trí
                    tuệ. Đây là cách thức bắt buộc đối với người nộp đơn
                    sáng chế mang quốc tịch nước ngoài. Đối với người
                    nộp đơn sáng chế có quốc tịch Việt Nam thì nộp đơn
                    thông quaTổ chức Đại diện sở hữu trí tuệ như
                    Công ty luật Việt An cũng được khuyến khích. Bởi lẽ
                    hồ sơ đăng ký sáng chế rất chuyên biệt và đòi
                    hỏiluật sư sở hữu trí tuệchuyên sâu để
                    hướng dẫn và tư vấn cụ thể.</li>
            </ul>
            <p><strong>Tại Việt Nam sáng chế được bảo hộ dưới các đối
                    tượng nào?</strong></p>
            <p>Việt Nam quy định về bằng sáng chế được chia thành 02 cấp
                độ để khuyến khích sự sáng tạo: Bằng sáng chế và bằng
                giải pháp hữu ích</p>
            <ul class="div-left-ul">
                <li>Bảo hộ sáng chế (Khi giải pháp đáp ứng điều kiện:
                    tính mới, tính sáng tạo và khả năng áp dụng công
                    nghiệp). Thời gian bảo hộ của sáng chế là 20 năm.</li>
                <li>Bảo hộ giải pháp hữu ích (Khi giải pháp đáp ứng điều
                    kiện: tính mới và khả năng áp dụng công nghiệp).
                    Thời gian bảo hộ của giải pháp hữu ích là 10 năm.</li>
            </ul>
            <h3 class="div-left-h3" id="Dịch_vụ_của_Luật_Việt_An_trong_lĩnh_vực_tư_vấn_đăng_ký_sáng_chế">Dịch
                vụ của Luật Việt An trong lĩnh vực tư vấn đăng ký sáng
                chế</h3>
            <ul class="div-left-ul">
                <li>Tư vấn, tra cứu, đánh giá khả năng sử dụng, đăng ký
                    bảo hộ của sáng chế / giải pháp hữu ích ở Việt Nam
                    và nước ngoài;</li>
                <li>Tra cứu không chính thức hoặc chính thức thông tin
                    sáng chế tại Cục Sở hữu trí tuệ – Chi phí độc lập
                    (Tra cứu tình trạng kỹ thuật liên quan đến Sáng chế/
                    Giải pháp hữu ích);</li>
                <li>Hoàn thiện hồ sơ xin cấp văn bằng bảo hộ Sáng chế
                    (dịch tài liệu hoặc viết bản mô tả sáng chế và yêu
                    cầu bảo hộ;</li>
                <li>Chuẩn bị các hình vẽ, làm tờ khai, đại diện) cho
                    khách hàng trong việc nộp đơn yêu cầu cấp bằng bảo
                    hộ ở Việt Nam và ở nước ngoài;</li>
                <li>Theo dõi tiến trình xét nghiệm đơn tại Cục Sở hữu
                    trí tuệ, soạn thảo công văn giấy tờ giao dịch với
                    Cục Sở hữu trí tuệ;</li>
                <li>Đại diện cho khách hàng trong toàn bộ quá trình xác
                    lập quyền và phúc đáp công văn trao đổi với Cục Sở
                    hữu trí tuệ về việc đăng ký sáng chế;</li>
                <li>Trao đổi, cung cấp thông tin cho khách hàng trong
                    tiến trình đăng ký bảo hộ sáng chế/giải pháp hữu
                    ích.</li>
                <li>Tư vấn và thực hiện dịch vụ duy trì hiệu lực văn
                    bằng bảo hộ sáng chế đã được cấp ở Việt Nam và ở
                    nước ngoài;</li>
                <li>Tư vấn và đánh giá khả năng vi phạm các quyền sáng
                    chế đang được bảo hộ;</li>
                <li>Đánh giá hiệu lực văn bằng bảo hộ sáng chế đã được
                    cấp ở Việt Nam và ở nước ngoài;</li>
                <li>Đàm phán, soạn thảo, thẩm định, đăng ký hợp đồng
                    chuyển giao quyền sử dụng hoặc quyền sở hữu các sáng
                    chế ở Việt Nam và ở nước ngoài.</li>
            </ul>
