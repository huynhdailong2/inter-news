                <li>Bản mô tả sáng chế/giải pháp hữu ích, bao gồm tên
                    sáng chế/giải pháp hữu ích, phần mô tả, phần ví dụ
                    minh hoạ, phần yêu cầu bảo hộ và phần tóm tắt;</li>
                <li>Hình vẽ minh hoạ (nếu có);</li>
                <li>Bản sao tài liệu xin hưởng quyền ưu tiên có xác nhận
                    của cơ quan nơi đơn ưu tiên được nộp (chỉ yêu cầu
                    đối với đơn xin hưởng quyền ưu tiên theo Công ước
                    Paris). Tài liệu này có thể được bổ sung trong thời
                    hạn 03 tháng tính từ ngày nộp đơn.</li>
                <li>Đối với các đơn PCT nộp vào quốc gia Việt Nam thì
                    ngoài các tài liệu nêu trên, cần phải cung cấp các
                    tài liệu sau đây: Công bố đơn PCT. Báo cáo Xét
                    nghiệm Sơ bộ Quốc tế (PCT/IPER/409) (nếu có). Thông
                    báo về những thay đổi liên quan tới đơn (PCT/IB/306)
                    (nếu có). Báo cáo Kết quả Tra cứu Quốc tế
                    (PCT/ISA/210)… Đối với trường hợp này, có thể bổ
                    sung bản gốc của Giấy Uỷ Quyền trong thời hạn là 34
                    tháng tính từ ngày ưu tiên.Các thông tin cần
                    cung cấp:</li>
                <li>Họ tên đầy đủ, địa chỉ và quốc tịch của người nộp
                    đơn;</li>
                <li>Họ tên đầy đủ, địa chỉ và quốc tịch của tác giả sáng
                    chế/giải pháp hữu ích;</li>
                <li>Tên của sáng chế/giải pháp hữu ích;</li>
                <li>Số đơn ưu tiên, ngày ưu tiên và nước ưu tiên của đơn
                    (trong trường hợp đơn có xin hưởng quyền ưu tiên);</li>
                <li>Số đơn quốc tế và số công bố đơn quốc tế (trong
                    trường hợp là đơn PCT nộp vào quốc gia Việt Nam).</li>
            </ul>
