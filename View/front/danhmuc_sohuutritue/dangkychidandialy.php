            <p>Đơn đăng ký có thể nộp trực tiếp hoặc gửi qua bưu điện tới trụ sở Cục Sở hữu trí.</p>
            <p>Bước 2: Thẩm định hình thức đơn đăng ký chỉ dẫn địa lý.</p>
            <p>Kiểm tra việc tuân thủ các quy định về hình thức đối với đơn, từ đó đưa ra kết luận đơn có được coi là
                hợp lệ hay không.</p>
            <p>Trường hợp hồ sơ có thiếu sót, Cục Sở hữu trí tuệ sẽ gửi thông báo cho người nộp hồ sơ và trong thời hạn
                01 tháng kể từ ngày ra thông báo, người nộp hồ sơ phải sửa chữa thiếu sót đó.</p>
            <p>Bước 3: Ra thông báo chấp nhận/từ chối chấp nhận đơn:</p>
