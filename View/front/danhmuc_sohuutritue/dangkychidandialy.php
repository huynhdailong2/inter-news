            </h3>
            <p>
                Chỉ dẫn địa lý là dấu hiệu dùng để chỉ sản phẩm có nguồn gốc từ khu vực, địa phương, vùng lãnh thổ hay
                quốc gia cụ thể. Điều kiện chung đối với chỉ dẫn địa lý được bảo hộ như sau:
