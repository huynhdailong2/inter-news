                <li>Trường hợp đơn không hợp lệ, Cục Sở hữu trí tuệ sẽ ra thông báo từ chối chấp nhận đơn.</li>
            </ul>
            <p>Bước 4: Công bố đơn đăng ký chỉ dẫn địa lý.</p>
            <p>Trong thời hạn 02 tháng kể từ ngày chấp nhận đơn hợp lệ, đơn sẽ được công bố trên Công báo sở hữu công
                nghiệp.</p>
            <p>Bước 5: Thẩm định nội dung đơn đăng ký chỉ dẫn địa lý.</p>
            <p>Thủ tục này nhằm đánh giá khả năng được bảo hộ của chỉ dẫn địa lý nêu trong đơn. Thời hạn thẩm định nội
                dung là 06 tháng kể từ ngày công bố đơn.</p>
            <p>Bước 6: Ra quyết định cấp/từ chối cấp văn bằng bảo hộ.</p>
            <ul class="div-left-ul">
                <li>Trường hợp đối tượng nêu trong đơn không đáp ứng được các yêu cầu về bảo hộ, Cục Sở hữu trí tuệ sẽ
                    ra quyết định từ chối cấp văn bằng bảo hộ;</li>
                <li>Trường hợp đối tượng nêu trong đơn đáp ứng được các yêu cầu về bảo hộ và người nộp đơn nộp lệ phí
                    đầy đủ thì Cục Sở hữu trí tuệ sẽ ra quyết định cấp văn bằng bảo hộ, ghi nhận vào Sổ đăng ký quốc gia
                    về sở hữu công nghiệp, và công bố trên Công báo Sở hữu trí tuệ.</li>
            </ul>
