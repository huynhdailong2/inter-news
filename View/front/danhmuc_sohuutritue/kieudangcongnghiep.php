                ảnh của kiểu dáng sẽ làm mất tính mới so với chính nó nên khi đăng ký
                sẽ bị từ chối cấp bằng bảo hộ.
            </p>
            <h4 class="div-left-h4" id="Kiểu_dáng_công_nghiệp_được_gia_hạn_tối_đa_bao_lần?">
