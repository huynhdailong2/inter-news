                Kiểu dáng công nghiệp được bảo hộ trong 05 năm sau đó được gia hạn
                tối đa 02 lần tiếp theo, sau đó kiểu dáng thuộc về công chúng không
                bảo hộ độc quyền cho chủ đơn nữa.
