                <li>Số phương án đăng ký càng nhiều, ảnh, hình vẽ đăng ký càng nhiều thì lệ phí nộp đơn càng tăng.</li>
                <li>Phân loại lớp kiểu dáng công nghiệp. Đơn đăng ký kiểu dáng công nghiệp cần được phân loại quốc tế về
                    kiểu dáng công nghiệp, trường hợp Người nộp đơn không phân loại hoặc phân loại không chính xác thì
                    Cục Sở hữu trí tuệ sẽ tiến hành phân loại và người nộp đơn cần nộp phí phân loại theo quy định
                    (100.000VNĐ/01 phân loại).</li>
            </ul>
            <h4 class="div-left-h4" id="Bước_2:_Tra_cứu_kiểu_dáng_công_nghiệp">
                <em>Bước 2: Tra cứu kiểu dáng công nghiệp</em>
            </h4>
            <p>
                Để đánh giá khả năng đăng ký thành công kiểu dáng công nghiệp
                tại Việt Nam cũng như tiết kiệm chi phí và thời gian của chủ đơn,
                chủ đơn nên tiến hành việc tra cứu kiểu dáng công nghiệp để đánh
                giá khả năng bảo hộ trước khi tiến hành nộp đơn đăng ký kiểu dáng công nghiệp.
            </p>
            <p>Ưu điểm tra cứu kiểu dáng công nghiệp</p>
            <ul class="div-left-ul">
                <li>Đánh giá chính xác khoảng 80% khả năng bảo hộ của kiểu dáng công nghiệp trước khi tiến hành việc nộp
                    đơn. Điều này giúp Quý Công ty cũng như Công ty luật Việt An có thể ước lượng được khả năng đăng ký
                    thành công của kiểu dáng công nghiệp trước khi nộp đơn đăng ký.</li>
                <li>Giả sử trong trường hợp kết quả tra cứu thể hiện khả năng thành công không cao, Công ty luật Việt An
                    sẽ cung cấp cho Quý Công ty dịch vụ tư vấn để có thể nâng cao khả năng đăng ký thành công. Trong
                    trường hợp kết quả tra cứu thể hiện khả năng thành công cao thì việc nộp đơn lúc đó rất an toàn và
                    chủ động nộp đơn đăng ký.</li>
            </ul>
            <h4 class="div-left-h4" id="Bước_3:_Nộp_đơn_đăng_ký_kiểu_dáng_công_nghiệp">
                <em>Bước 3: Nộp đơn đăng ký kiểu dáng công nghiệp</em>
            </h4>
            <p>Đơn đăng ký kiểu dáng công nghiệp bao gồm các hồ sơ sau:</p>
            <ul>
                <li>Tờ khai đăng ký kiểu dáng công nghiệp được làm theo mẫu của Cục Sở hữu trí tuệ ban hành (in 03 bộ).
                    Vui lòng liên hệ trực tiếp Luật Việt An để được cung cấp mẫu tờ khai theo quy định.</li>
                <li>Giấy uỷ quyền (theo mẫu của Công ty luật Việt An);</li>
                <li>Bộ ảnh chụp hoặc bộ ảnh vẽ kiểu dáng công nghiệp thể hiện được đầy đủ các góc nhìn của đối tượng cần
                    đăng ký bảo hộ của kiểu dáng công nghiệp;</li>
                <li>Bản mô tả kiểu dáng công nghiệp (in 03 bộ);</li>
                <li>Trong trường hợp trên kiểu dáng có chứa các dấu hiệu nhãn hiệu: người nộp đơn cần nộp tài liệu xác
                    nhận quyền sở hữu nhãn hiệu. Cụ thể: Tài liệu xác nhận quyền nộp đơn đăng ký nhãn hiệu hợp pháp, nếu
                    người nộp đơn thụ hưởng quyền nộp đơn của người khác (Giấy chứng nhận quyền thừa kế; Giấy chứng nhận
                    hoặc Thoả thuận chuyển giao quyền nộp đơn đăng ký nhãn hiệu; Hợp đồng giao việc hoặc Hợp đồng lao
                    động) (01 bản);</li>
                <li>Trường hợp đơn đăng ký kiểu dáng công nghiệp xin hưởng quyền ưu tiên cần cung cấp thêm bản sao đơn
                    đầu tiên hoặc tài liệu chứng nhận trưng bày tại triển lãm, nếu trong đơn có yêu cầu được hưởng quyền
                    ưu tiên theo Điều ước quốc tế (01 bản). Quyền ưu tiên chỉ áp dụng cho đơn đăng ký kiểu dáng công
                    nghiệp của tổ chức, cá nhân nước ngoài tại Việt Nam;</li>
            </ul>
            <p>
                Tại thời điểm nộp đơn đăng ký kiểu dáng công nghiệp chủ đơn chưa
                có đủ hồ sơ gốc, để lấy ngày ưu tiên sớm chủ đơn có thể nộp sau
                các giấy tờ gốc theo thời hạn như sau:
            </p>
            <ul class="div-left-ul">
                <li>Giấy uỷ quyền bản gốc có thể nộp sau trong vòng 01 tháng kể từ ngày nộp đơn. Tuy nhiên, khi nộp đơn
                    vẫn cần nộp bản phô tô giấy uỷ quyền;</li>
                <li>Một số tài liệu có thể nộp trong thời hạn 03 tháng kể từ ngày nộp đơn như: Bản tiếng Việt của bản mô
                    tả kiểu dáng công nghiệp, nếu trong đơn đã có bản tiếng Anh/Pháp/Nga của tài liệu đó; Chứng từ nộp
                    phí nộp đơn và phí công bố đơn.</li>
            </ul>
            <h4 class="div-left-h4" id="Bước_4:_Thẩm_định_hình_thức_đơn_đăng_ký_kiểu_dáng_công_nghiệp">
                <em>Bước 4: Thẩm định hình thức đơn đăng ký kiểu dáng công nghiệp</em>
            </h4>
            <ul class="div-left-ul">
                <li>Thời hạn thẩm định hình thức:&nbsp;01 tháng kể từ ngày nộp đơn.</li>
                <li>Cục Sở hữu trí tuệ sẽ xem xét đơn có đủ điều kiện về hình thức, ảnh, chủ sở hữu đơn, quyền nộp đơn,
                    phân loại,…</li>
                <li>Nếu đơn đăng ký của doanh nghiệp đáp ứng điều kiện, Cục Sở hữu trí tuệ sẽ Thông báo chấp nhận đơn
                    hợp lệ và cho đăng công bố đơn.</li>
                <li>Nếu đơn đăng ký kiểu dáng công nghiệp của doanh nghiệp không đáp ứng điều kiện, Cục Sở hữu trí tuệ
                    sẽ ra Thông báo không chấp nhận đơn và đề nghị doanh nghiệp sửa đổi. Doanh nghiệp tiến hành sửa đổi
                    theo yêu cầu và nộp công văn sửa đổi cho Cục Sở hữu trí tuệ.</li>
            </ul>
            <h4 class="div-left-h4" id="Bước_5:_Công_bố_đơn_đăng_ký_kiểu_dáng_công_nghiệp">
                <em>Bước 5: Công bố đơn đăng ký kiểu dáng công nghiệp</em>
            </h4>
            <ul class="div-left-ul">
                <li>Thời hạn công bố đơn đăng ký kiểu dáng công nghiệp:02 tháng kể từ ngày có Thông báo chấp nhận đơn
                    hợp lệ.</li>
                <li>Nội dung công bố đơn đăng ký kiểu dáng công nghiệp là các thông tin liên quan đến đơn hợp lệ ghi
                    trong thông báo chấp nhận đơn hợp lệ, ảnh và phân loại kiểu dáng công nghiệp.</li>
            </ul>
            <h4 class="div-left-h4" id="Bước_6:_Thẩm_định_nội_dung_đơn_đăng_ký_kiểu_dáng_công_nghiệp">
                <em>Bước 6: Thẩm định nội dung đơn đăng ký kiểu dáng công nghiệp</em>
            </h4>
            <ul class="div-left-ul">
                <li>Thời hạn thẩm định nội dung: 09 -12 tháng kể từ ngày công bố đơn.</li>
                <li>Cục Sở hữu trí tuệ xem xét các điều kiện đăng ký kiểu dáng công nghiệp từ đó đánh giá khả năng cấp
                    văn bằng cho kiểu dáng công nghiệp cho chủ đơn đăng ký. Nếu đơn đăng ký kiểu dáng công nghiệp đáp
                    ứng đủ điều kiện thì Cục Sở hữu trí tuệ ra Thông báo dự định cấp văn bằng cho kiểu dáng công nghiệp
                    đã đăng ký.</li>
                <li>Nếu đơn đăng ký kiểu dáng công nghiệp không đáp ứng đủ điều kiện, Cục Sở hữu trí tuệ ra Thông báo
                    không cấp văn bằng cho kiểu dáng công nghiệp mà doanh nghiệp đăng ký. Trong trường hợp này, chủ đơn
                    có thể xem xét và gửi công văn trả lời, khiếu nại quyết định của Cục Sở hữu trí tuệ, đồng thời đưa
                    ra các căn cứ để cấp văn bằng bảo hộ kiểu dáng công nghiệp cho kiểu dáng công nghiệp của doanh
                    nghiệp.</li>
            </ul>
            <h4 class="div-left-h4" id="Bước_7:_Cấp_văn_bằng_bảo_hộ_kiểu_dáng_công_nghiệp">
